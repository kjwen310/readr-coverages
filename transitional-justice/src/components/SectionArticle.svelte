    <h1 id="article-h1">白色恐怖時期 上萬平民接受「軍法」審判</h1>
    <p>民國 38 年，國民黨政府從中國撤退到臺灣之後，隨即宣告臺灣進入近 40 年的戒嚴時期。這段期間，政府授權情治機關嚴密控制社會，包括禁止集會結社、查扣出版品、審核人身行動，以及將平民交付軍法審判，導致上萬名政治犯或是深陷牢獄，或是喪失性命。</p>
    <p>為什麼平民也要接受「軍法」審判？促轉會兼任委員、中原大學財法系副教授徐偉群解釋，民國 38 年後，臺灣也成為國共內戰的戰場之一，依《<a href="https://law.moj.gov.tw/LawClass/LawAll.aspx?PCode=F0070002" target="_blank" rel="noreferrer noopener">戒嚴法</a>》規定，區域內的民眾只要犯下內亂、外患、妨害秩序等罪，軍事機關可以自行審判或交法院審判。</p>
    <p>但儘管合法，軍審制度卻有很多問題。如權責分立嚴重失衡，徐偉群表示，「臺灣警備總司令部」雖是國防部轄下的軍事機關，卻身兼多種功能，包括有偵蒐權的情治單位、維護公共安全的治安機關、以及軍審案件的起訴機關與審理機關，政治偵防、治安、起訴到審判都一手包辦，從現代法治國家的視角看來，「簡直不可思議，怎麼可能讓一個機關同時負責多項重要的任務」，完全沒有落實審檢分立和獨立審判。</p>
    <p>此外，軍事審判在犯人的罪行認定上也常常不一致。資料庫中，情治機關最常起訴受難者的原因是「意圖顛覆政府，並付諸行動」<span class="hint" on:click={() => handleHintClick('1')}>{hintToggle['1'] ? 'X' : '註'}</span>，此條文出自原本的《刑法》第 100 條第 1 項，觸法者可處 7 年以上有期徒刑，首謀者處無期徒刑；戒嚴時期，如果再加上《懲治叛亂條例》第 2 條第 1 款規定，犯《刑法》第 100 條第 1 項等罪者，即處死刑。</p>
    {#if hintToggle['1']}
    <div class="hint-wrapper">註：起訴書的原文為「意圖破壞國體，竊據國土，或以非法之方法變更國憲，顛覆政府，而著手實行」。</div>
    {/if}
    <p>但這麼重的罪，違法與否的判斷依據卻非常模糊，徐偉群表示，什麼是「意圖」？做到怎樣的程度才算是「著手實行」？當法條內容太抽象，就很容易被無限擴張。臺北教育大學臺灣文化研究所助理教授蘇瑞鏘補充，只有政府有權力解讀什麼行為才構成叛亂，所以老百姓說的任何話、寫的任何信，都可以被解讀是著手實行，「這是白色恐怖最可怕的地方」。</p>
    <p>以「綠島獄中再叛亂案」為例，當時有政治犯因互相傳遞寫有左翼內容的紙條，在獄中被舉報。徐偉群提到，初審軍法官認為傳紙條並不構成叛亂，只要「矯正思想」就好；結果上交到時任參謀總長彭孟緝的時候，他詢問蔣介石，得到回覆「這些人已有叛亂行為，在獄中竟然還繼續叛亂，必須嚴為復審」，最後便以「意圖顛覆政府，並付諸行動」<span class="hint" on:click={() => handleHintClick('2')}>{hintToggle['2'] ? 'X' : '註'}</span>為理由，槍決 14 名政治犯。</p>
    {#if hintToggle['2']}
    <div class="hint-wrapper">註：起訴書的原文為「意圖破壞國體，竊據國土，或以非法之方法變更國憲，顛覆政府，而著手實行」。</div>
