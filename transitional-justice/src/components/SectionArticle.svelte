    <h2 id="article-h2-2">有審判者揣摩上意 有人提出異議就被懲處</h2>
    <p>中研院臺史所助研究員林正慧指出，時任總統府參軍長劉士毅曾在公文中提到，會定時監督軍法官審判的過程，如果有政治犯判處死刑，也會附上行刑前後的照片，以回應蔣介石的要求；周至柔也在公文中寫道，是呈蔣介石的想法才改判重刑。</p>
    <p>因此，林正慧認為，不能排除軍政高層諸如周至柔、劉士毅與桂永清等人在審理政治案件時，會選擇加重政治犯的刑度，以防遭蔣介石檢討。</p>
    <p>不過，仍有部分審理人不同意蔣介石的判決，像是民國 58 年的「統中會案」，有民眾私下籌組社團被查獲，軍法官認定其涉嫌顛覆政權，刑度應加重至無期徒刑。案件呈交到總統府秘書長張羣、總統府參軍長高魁元等人手中時，他們認為判處 15 年有期徒刑即可，便在公文上批註「不同意」。但最後在蔣介石的堅持下，涉案的政治犯呂昱、周順吉、莊信男 3 人仍處無期徒刑。</p>
    <p>提出異議後，有審理人成功變更判決、有人失敗，也有人因此被懲處。蘇瑞鏘舉例，前臺北師範學院學生陳正宸遭指控參與叛亂組織，初審判決無罪，之後蔣介石要求重新審判，還說判學生無罪的主審官「應有相當處分」，後來該主審官被記過，陳正宸也判處死刑，可見整個軍審體系彼時籠罩在巨大的政治壓力之下。</p>
    <p>制度上，軍審結果必須呈核軍事長官與總統，使得各級審理人在裁奪刑度時，無法就案論案；實務上，蔣介石高度介入判決過程，也讓軍事審判毫無獨立可言，淪為統治者的鎮壓工具。促轉會副主委葉虹靈指出，這些剝奪人民基本權利的國家暴力都於法有據，「一切都是合法的」，因此軍審決策者可以宣稱自己是依法行政。</p>
    <h2 id="article-h2-3">轉型正義應回到「人」 而非善惡的標籤</h2>
    <p>葉虹靈強調，促轉會最大的挑戰，即是定調威權政府的哪些行為，已經違反法治國家應賦予民眾的基本保障，「畢竟當時的政府為了要維護政權，處理很多案件時，其實不會兼顧有沒有合法」，如果未來再發生政府聲稱民眾涉嫌叛亂，以「國家安全」名義要求速審速決時，法律要保障民眾的基本人權，而不是像戒嚴時期，縱容政府濫用司法權力。</p>
    <p>而定調之前，得先對陌生的加害者結構有初步的理解，葉虹靈認為，要先「釐清加害體制圖像」，包括認識軍事審判體制的全貌、理解軍事審判官的角色，以及上層長官分別有著怎麼樣的權力？第二個層次是「揭露加害行為」，參與軍審的決策者在什麼樣的位子上做出哪些決策？接下來才是「釐清誰是加害者」，以及加害者該負的法律責任。</p>
    <p>葉虹靈坦承，轉型正義資料庫仍有其限制，只有收錄有姓名和審判流程的案件，缺乏更多細節：如案件如何生成？情治機關如何偵訊、逮捕？這部分較難標準化。林正慧也認為，資料庫並沒有處理到情治機關逮捕、偵訊的過程，她從保密局檔案研究中發現，很多人在偵訊時就已被判刑，後續的審判只是形式上的流程，資料庫的資料如何跟情治逮捕偵訊的過程扣連，是可以嘗試的方向。</p>
    <p>「加害者體制不能只放在臺灣，還要拉到中國的近代史來看。」蘇瑞鏘表示，威權統治是國民黨撤退來臺灣之前就逐漸形成的系統，像《刑法》是國民黨於民國 24 年在中國訂定的法律，而情治體系來到臺灣後，也只是改名，組織成員都是一樣的，「國民黨在中國怎麼做，來到臺灣當然繼續做一樣的事，加上要抓共產黨，更會加強力道。」想看清楚全貌，可能要往前回溯，看看加害者體制是如何被建構出來。</p>
    <p>白色恐怖時期，大量平民受到軍事審判，究竟誰受難、如何受難、誰負責審判、又是誰加重或減輕政治犯的刑責，葉虹靈強調，如果沒有釐清每個行動者在威權體制下，做出任何行為的真正原因，就很容易簡化歷史，流於只批評兩蔣、中下層行動者只能服從上意，或是軍政高層全部都是黨國鷹犬等刻板印象，「會做這個資料庫，就是希望回到檔案本身，帶領民眾回到歷史現場。」</p>
