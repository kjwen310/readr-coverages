    <h2 id="article-h2-1">對判決不滿意就退回 蔣介石成最高主宰</h2>
    <p>而軍審制度最大的爭議，是判決的結果必須呈交給軍事長官和總統核覆。徐偉群解釋，如果他們認為判決不合理，可以要求軍事法庭重新審判（行使「覆核權」），這樣的設計讓前總統蔣介石能以三軍統帥的身分，成為體制中的頂點，其他軍審單位大多得聽命行事。</p>
    <p>「覆核權」已經很爭議，但蔣介石更常違法直接下達判決指示，讓覆核權形同虛設。</p>
    <p>蘇瑞鏘強調，軍事長官和總統如果不同意判決結果，就得提出理由，而且只能要求重新審判，不得變更判決；可是蔣介石不會交代駁回的原因，反而寫下「槍決可也」、「凡判處 12 年以上徒刑者一律改處死刑」等批註，「找不到任何一條法條可以讓蔣介石這樣做，他還是做了。」</p>
